// Primitive Data Types

// String: textual data enclosed using '',""
let firstName = "John"
console.log("Firstname:", firstName)

// typeof operator
console.log("type of firstName variable:", typeof firstName)

// Number: represents numeric value
// 30, -30, 3.14
let age = 30;
console.log("Age:", age)
console.log("Type of Age:", typeof age)

//Boolean: Either true or false
let isAdmin  = false;
console.log("Is User an Admin:", isAdmin)
console.log("Type of isAdmin:", typeof isAdmin)
// Booleans can only be true or false

// Null: Intentional absence of a value
let car = null;
console.log("Car:", car)
console.log("Type of Car", typeof car);

// Undefined: Unintentional absence of value
let city;
console.log("City", city)
console.log("Data Type of City:", typeof city)